// Calculadora basica

var primerNumero = Number(prompt("Ingrese el primer numero", "0"))

var segundoNumero = Number(prompt("Ingrese el segundo numero", "1"))

var sumar = primerNumero + segundoNumero

var restar = primerNumero - segundoNumero

var multiplicar = primerNumero * segundoNumero

var dividir = primerNumero / segundoNumero

alert("La suma es: "+sumar)

alert("La diferencia es: "+restar)

alert("El producto es: "+multiplicar)

alert("El cociente es: "+dividir)
